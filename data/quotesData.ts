// Simple helper functions for backwards compatibility
// Helper to convert raw quote to full Quote with ID
const toQuote = (q: Omit<Quote, 'id' | 'isCustom'>, idx: number): Quote => ({
    ...q,
    id: `quote-${q.category}-${idx}`,
    isCustom: false,
});

export const getAllQuotes = (): Quote[] =>
    INITIAL_QUOTES.map((q, i) => toQuote(q, i));

export const getQuotesByCategory = (category: string): Quote[] =>
    INITIAL_QUOTES.filter(q => q.category === category).map((q, i) => toQuote(q, i));

export const getRandomQuote = (): Quote | null => {
    if (INITIAL_QUOTES.length === 0) return null;
    const idx = Math.floor(Math.random() * INITIAL_QUOTES.length);
    const quote = INITIAL_QUOTES[idx];
    return quote ? toQuote(quote, idx) : null;
};

export const getRandomQuotes = (count: number): Quote[] => {
    const shuffled = [...INITIAL_QUOTES].map((q, i) => toQuote(q, i)).sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
};
